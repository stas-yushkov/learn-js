const sum = function (a, b) {
  console.log('a + b = ', a + b);
};

const anotherHigherOrderComponent = function (someFunction) {
  const number1 = 2;
  const number2 = 3;
  someFunction(number1, number2);
};

anotherHigherOrderComponent(sum);
